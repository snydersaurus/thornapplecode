    private class SongListModel extends AbstractListModel{
        List songs;
        public SongListModel(List songs){
            this.songs = songs;
        }
        
        public int getSize() {
            return songs.size();
        }
        
        public Object getElementAt(int index) {
            return songs.get(index);
        }
    }
