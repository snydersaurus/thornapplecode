    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            List results = null;
            if (txtSongName.getText().trim().length() > 1)
                results = searchService.findSongsByName(txtSongName.getText());
            else
                results = searchService.findSongsByArtistName(txtArtistName.getText());
            jList2.setModel(new SongListModel(results));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
