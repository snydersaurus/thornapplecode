    private void txtSongNameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtSongNameMouseClicked
        if (!songInit && "type song name".equals(txtSongName.getText())){
            txtSongName.setText("");
            songInit = true;
        }
    }//GEN-LAST:event_txtSongNameMouseClicked

